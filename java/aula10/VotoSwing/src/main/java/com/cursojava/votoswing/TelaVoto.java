    private void SpinnerAnoNasciStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SpinnerAnoNasciStateChanged
        // TODO add your handling code here:
        int year = LocalDate.now().getYear();
        int anoNasci = Integer.parseInt(SpinnerAnoNasci.getValue().toString());
        int idade = (year - anoNasci);
        txtIdade.setText("Idade: " + idade);
        if (idade < 16){
            txtVoto.setText("Voto Proibido");
        } else if ((idade >= 16 && idade < 18)  || idade > 70){
            txtVoto.setText("Voto Opcional");
        } else{
            txtVoto.setText("Voto Obrigat√≥rio");
        }
    }//GEN-LAST:event_SpinnerAnoNasciStateChanged

